      <p>Coleman, Gledhill, Hargrave, Merritt & Rainsford, P.C., traces its roots back to 1969 in Hillsborough 
        and Chapel Hill, North Carolina when the firm was named Winston, Coleman & Bernholz and had offices 
        on Churton Street in Hillsborough and on Franklin Street in Chapel Hill.  The senior member of the 
        firm was A. B. “Lonnie” Coleman, Jr. </p>
      <p> On January 1, 1980, Gledhill and Hargrave were made partners and the firm changed its name to 
        Coleman, Bernholz, Bernholz, Dickerson, Gledhill & Hargrave. In 1988, the firm moved its office in 
        Hillsborough from Churton Street to its current location on East Tryon Street, into what was the old 
        Piedmont Electric Membership Corporation building.  In 1990, the firm divided its offices and 
        Lonnie Coleman, Geof Gledhill and Doug Hargrave formed the partnership of Coleman, Gledhill & Hargrave in 
        Hillsborough.  The other former members of the firm regrouped as a new law firm in Chapel Hill.</p>
      <p> In 1996, Lonnie was appointed by Governor Jim Hunt as a District Court Judge for Judicial District 
        15B, serving Orange and Chatham counties.  His son, Sam Coleman replaced him as an owner and 
        officer in the firm. After being an attorney with the firm for three (3) years, Katherine “Katie” 
        Merritt became an owner and officer in the fall of 2011. The firm’s name was briefly changed to Coleman, 
        Gledhill, Hargrave, Peek & Merritt, P.C.</p>
      <p>James Rainsford, after working two years as an attorney for the firm, became a partner/officer in early 
        2014. The firm name then changed to Coleman, Gledhill, Hargrave, Merritt & Rainsford, P.C. At present 
        the firm has 8 attorneys, 9 permanent staff members and a number of clerks, interns and part-time
         employees. Collectively our staff represents more than 245 years of experience in the legal field.</p>
